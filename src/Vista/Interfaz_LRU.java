    public void mostrarMatriz() {
        LRU lru = new LRU();
        lru.setCantidadMarco(Integer.parseInt(jTextFieldMPagina.getText()));
        lru.setCantidadPaginas(Integer.parseInt(jTextFieldNPaginas.getText()));
        lru.setPaginas(cantidadpaginas);
        lru.lru();
        int cantidadMarco = Integer.parseInt(jTextFieldMPagina.getText());
        int cantidadPaginas = Integer.parseInt(jTextFieldNPaginas.getText());
        String matriz[][] = lru.RetornarMatriz();
        jTableAlgoritmo.removeAll();
        Object vector[] = new Object[cantidadPaginas];

        for (int i = 0; i < cantidadPaginas; i++) {
            vector[i] = "Pag" + (i + 1);
        }
        DefaultTableModel modelo = new DefaultTableModel(null, vector);
        String arreglo[] = new String[cantidadPaginas];
        for (int i = 0; i < cantidadMarco; i++) {
            for (int j = 0; j < cantidadPaginas; j++) {
                  
               if (matriz[i][j].equals("-")) {
                 arreglo[j] = " ";
              } else {
                 arreglo[j] = matriz[i][j];
              }
            }
            modelo.addRow(arreglo);
            jTableAlgoritmo.setModel(modelo);
        }
        int fallos[] = lru.RetornarVector();
        for (int i = 0; i < fallos.length; i++) {
            arreglo[i] = String.valueOf(fallos[i]);
        }
        modelo.addRow(arreglo);
        jTableAlgoritmo.setModel(modelo);
        jLabelNFallos.setText(String.valueOf(lru.contFallos()));
    }
    

