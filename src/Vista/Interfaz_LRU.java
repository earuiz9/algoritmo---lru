    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        // TODO add your handling code here:
        if (jTextFieldNPaginas.getText().equals("") || jTextFieldMPagina.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese datos porfavor");
        } else {

            int cp = Integer.parseInt(jTextFieldNPaginas.getText());
            cantidadpaginas = new String[cp];
            for (int c = 0; c < cp; c++) {
                cantidadpaginas[c] = (JOptionPane.showInputDialog(null, "Ingrese valor de paginas [" + (c + 1) + "]"));
                valores.add(cantidadpaginas[c]);
                lista.addElement(String.valueOf(valores.get(c)));

            }
            jListNpaginas.setModel(lista);
            jButtonAAlgortimo.setEnabled(true);
        }
    }//GEN-LAST:event_jButtonAgregarActionPerformed

